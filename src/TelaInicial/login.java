    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed

        String login = txtLogin.getText();
        String password = String.valueOf(txtSenha.getPassword());
 
        boolean achou = false;
     
        for(Adm a : listaAdm){
            if((a.getCpf().equalsIgnoreCase(login)) && (a.getSenha().equals(password))){
                 achou = true;
                 //entra tela do ADM
                 this.dispose();
            }
        }
        
        for (Cliente cli : listaCliente){
            System.out.println(cli);
            if((cli.getCpf().equalsIgnoreCase(login)) && (cli.getSenha().equals(password))){                
                achou = true;
                TelaPrincipalCliente t =new TelaPrincipalCliente(cli);
                t.setVisible(true);
                this.dispose();
            }  
        }
       
        if(!achou){
            JOptionPane.showMessageDialog(null, "Login ou Senha Inv√°lidos");
        }
        
    
    }//GEN-LAST:event_btnEntrarActionPerformed

