    private void btnCadastrar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrar1ActionPerformed
    //        CadastroAgenda dialog = new CadastroAgenda(new javax.swing.JFrame(), true);
//        dialog.mostrarCadastroAgenda(this);

        //verifica se esta tudo preenchido
        if(txtNome.getText().isEmpty() || txtBairro.getText().isEmpty() || 
           txtRua.getText().isEmpty() || txtSenha.getPassword().length==0 || 
           txtEmail.getText().isEmpty() || txtNumero.getText().isEmpty() ||
           txtCEP.getText().equals("     -   ")|| txtNascimento.getText().equals("  /  /    ")||
           txtTelefone.getText().equals("(  )      -    ") || txtCPF.getText().contains(" ")){
           JOptionPane.showMessageDialog(null, "Preencha todos os dados corretamente!");      
        }else{
            //verifica se o cliente existe
            if(new Cliente().procurarCliente(txtCPF.getText())==null){
                //pega data de nascimento
                int dia=Integer.parseInt(txtNascimento.getText().substring(0, 2));
                int mes=Integer.parseInt(txtNascimento.getText().substring(3, 5));
                int ano=Integer.parseInt(txtNascimento.getText().substring(6, 10));

                LocalDate nascimento = LocalDate.of(ano, mes, dia);

                //pega endere√ßo
                String endereco="Rua: "+txtRua.getText()+" Bairro: "+txtBairro.getText()
                                +" Numero: "+txtNumero.getText()+" Cidade :"+jBoxCidade.getSelectedItem()
                                +" Estado:"+jBoxEstado.getSelectedItem();
                
            
                //cria o novo cliente
                Cliente c = new Cliente(txtNome.getText(),
                txtCPF.getText(),String.valueOf(txtSenha.getPassword()),txtEmail.getText(),
                endereco, nascimento);
                //salva o cliente
                new ObjectDao().save("cliente", c);
                
                JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso");
                
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "CPF ja cadastrado");
            }
        }

    }//GEN-LAST:event_btnCadastrar1ActionPerformed

