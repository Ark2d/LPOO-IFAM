    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        ObjectDao obj = new ObjectDao();
        Categoria c;
        if(new File("src\\Dados\\categoria.dat").exists()){
            int codigo=0;            
            List<Categoria> list = (List<Categoria>)(Object)obj.show("categoria");
            new File("src\\Dados\\categoria.dat").delete();
            for (Categoria categoria : list) {
                if(codigo==index){                    
                    c = new Categoria(list.get(index).getCodigo(), txtNome.getText());
                    obj.save("categoria", c);
                    codigo++;
                    continue;
                }
                obj.save("categoria", categoria);
                codigo++;
            }
            
        }
        
        JOptionPane.showMessageDialog(null, "Categoria criada com sucesso!");
        this.dispose();
    }//GEN-LAST:event_btnAlterarActionPerformed

