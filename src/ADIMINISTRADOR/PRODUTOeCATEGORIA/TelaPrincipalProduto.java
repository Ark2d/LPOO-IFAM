    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(0==JOptionPane.showConfirmDialog
            (this,"Tem certeza que deseja\n"
                + "excluir "+(String)this.tabelaProdutos.getValueAt(this.tabelaProdutos.getSelectedRow(), 1),
                  "Finalizando Compra!",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE)
            ){
            String codigo = (String)this.tabelaProdutos.getValueAt(this.tabelaProdutos.getSelectedRow(), 0);
            ObjectDao obj = new ObjectDao();
            List<Produto> listaProdutos =(List<Produto>)(Object)obj.show("produto");
            new File("src\\Dados\\produto.dat").delete();
            for (Produto produto : listaProdutos) {
                if(produto.getCodigo().equalsIgnoreCase(codigo))
                    continue;                
                obj.save("produto", produto);
            }
            
          JOptionPane.showMessageDialog(this,"Produto excluido com sucesso!");
          carregarProdutos();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

