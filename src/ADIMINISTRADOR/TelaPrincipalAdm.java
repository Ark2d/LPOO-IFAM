    private void carregarCompras(){
        List<Compra> lista = (List<Compra>)(Object)new ObjectDao().show("compra");
        DefaultTableModel model = (DefaultTableModel)tabelaCompras.getModel();
        DecimalFormat df = new DecimalFormat("#,##0.00");
        for (Compra compra : lista) {
            String codigo = Integer.toString(compra.getId());
            String nome = compra.getCliente().getNome();
            String valor = df.format(compra.getValorTotal());
            String status = compra.getStatus();
            model.addRow(new String[]{codigo,nome,("R$ "+valor),status});
        }
        tabelaCompras.setModel(model);
        tabelaCompras.getTableHeader().setReorderingAllowed(false);
    }

