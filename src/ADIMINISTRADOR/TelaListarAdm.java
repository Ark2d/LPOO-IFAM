    public void carregarAdm(){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nome");
        modelo.addColumn("CPF");
        modelo.addColumn("Email");
        
        for (Adm ad : listaAdm){
            modelo.addRow(new Object[]{ad.getNome(),
                                       ad.getCpf(),
                                       ad.getEmail()});
        }
        jTableAdm.setModel(modelo);      
    }
    
