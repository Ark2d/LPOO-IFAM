    private void JButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonEditarActionPerformed
         if(0==JOptionPane.showConfirmDialog
            (this,"Tem certeza que deseja\n"
                + "excluir "+jTableAdm.getValueAt(jTableAdm.getSelectedRow(), 0),
                  "Excluir Administrador",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE)
            ){
            ObjectDao sv = new ObjectDao();
            List<Adm> listaAdm = (List<Adm>)(Object)sv.show("adm");
            new File("src\\Dados\\adm.dat").delete();
            int i = 0;
            for (Adm adm : listaAdm) {
                if(i==jTableAdm.getSelectedRow()){
                    i++;
                    continue;
                }
                sv.save("adm", adm);
                i++;
            }
        }
        carregarAdm();
    }//GEN-LAST:event_JButtonEditarActionPerformed

